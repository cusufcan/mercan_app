String getDayName(int index) {
  switch (index) {
    case 0:
      return 'Pazartesi';
    case 1:
      return 'Salı';
    case 2:
      return 'Çarşamba';
    case 3:
      return 'Perşembe';
    case 4:
      return 'Cuma';
    case 5:
      return 'Cumartesi';
    case 6:
      return 'Pazar';
    default:
      return 'N/A';
  }
}
